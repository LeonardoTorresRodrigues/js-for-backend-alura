// No caso de números, é possível salvar em uma variável não somente o número em si, mas também o resultado de uma operação.

const soma = 10 + 10;
const multiplicacao = 10 * 10;
const operacao = (soma + multiplicacao) + 10; // utilizando parênteses () para seguir a regra matemática de precedência nas operações.

console.log(soma);
console.log(multiplicacao);
console.log(operacao);